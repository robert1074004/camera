const express = require('express')
const router = express.Router()
const passport = require('../config/passport')
const admin = require('./modules/admin')
const equipments = require('./modules/equipments')
const userController = require('../controllers/user-controller')
const { authenticated, authenticatedAdmin } = require('../middleware/auth')
const { generalErrorHandler } = require('../middleware/error-handler')
const { signUpValidator, editUserValidator, formError } = require('../middleware/validator')
const upload = require('../middleware/multer')
router.use('/admin', authenticatedAdmin, admin)
router.use('/equipments', authenticated, equipments)
router.get('/sign_up', userController.SignUpPage)
router.post('/sign_up', upload.single('userImg'), signUpValidator, formError, userController.SignUp)
router.get('/validate', userController.validatePage)
router.post('/validate', userController.validate)
router.get('/log_in', userController.logInPage)
router.post('/log_in', passport.authenticate('local', { failureRedirect: '/log_in', failureFlash: true }), userController.logIn)
router.get('/logout', userController.logout)
router.get('/aboutUs', (req, res) => { res.render('aboutUs') })
router.get('/editUser', authenticated, userController.editUserPage)
router.patch('/editUser/:id', upload.single('userImg'), editUserValidator, formError, userController.editUser)

router.get('/', authenticated, (req, res) => res.redirect('/equipments/:category'))
router.use('/', generalErrorHandler)
module.exports = router
